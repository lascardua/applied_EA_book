##########################################################################
#               Rosenbrock function
##########################################################################
# Input:
#   xx = [x1, x2, ..., xd]
# Minimum:
#   f(x^{\star}) = 0 at x^{\star} = (1,1,....,1)
##########################################################################
def rosenbrock(xx):
    d = len(xx)
    sum = 0
    for ii in range(d-1):
        xi = xx[ii]
        xnext = xx[ii+1]
        new = 100*(xnext-xi**2)**2 + (xi-1)**2
        sum = sum + new
    return sum

